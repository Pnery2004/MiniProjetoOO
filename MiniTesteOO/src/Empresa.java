
public class Empresa {
 private int CEP;
 private String Endereco;
 private String CNPJ;
 
public int getCEP() {
	return CEP;
}
public void setCEP(int cEP) {
	CEP = cEP;
}
public String getEndereco() {
	return Endereco;
}
public void setEndereco(String endereco) {
	Endereco = endereco;
}
public String getCNPJ() {
	return CNPJ;
}
public void setCNPJ(String string) {
	CNPJ = string;
}	
 
}
